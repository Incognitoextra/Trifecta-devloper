public class Variables {
    // On apprend a connaitre les variables 
    public static void main(String[] args) {
        // Declaration et initialisation des variables
        int nombre = 123; // recoit des nombre entier
        double reel = 12.3; // recoit des nombres entier et decimal
        char caractere = 'A'; // ne peut contenir qu'un seul caractere
        String c = "bonjour" ; // recoit les chaines de caracteres
        float decimal = 3.14f; // recoit des nombres decimaux
        boolean estVrai = true; // recoit les valeurs vrai(true) ou faux(false)
        long superLong = 10000000; // recoit les grands chiffres (millions, milliards etc..)
        short s = 14; // recoit des chiffres court

        // Utilisation des variables
        System.out.println("Utlisation de nos variables");
        System.out.println("Int = " + nombre);
        System.out.println("Double = " + reel);
        System.out.println("char = " + caractere);
        System.out.println("String = " + c);
        System.out.println("float = " + decimal);
        System.out.println("boolean = " + estVrai);
        System.out.println("long = " + superLong);
        System.out.println("short = " + s);
    }
}

